import json
import shutil
import subprocess
import sys
from datetime import datetime
from pathlib import Path

from orion.config import BASE_DIR
from orion.config import get_os as _get_os
import contextlib


def _base_dir() -> Path:
    return BASE_DIR


def _scripts_dir() -> Path:
    d = Path.home() / ".orion" / "reminders"
    d.mkdir(parents=True, exist_ok=True)
    return d


def _sanitise(text: str, max_len: int = 200) -> str:
    return (
        text.replace("\\", "")
        .replace('"', "")
        .replace("'", "")
        .replace("\n", " ")
        .replace("\r", "")
        .strip()
    )[:max_len]


